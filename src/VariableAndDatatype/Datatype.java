package VariableAndDatatype;

/* java have 2 type of datatype : 1)primitive
                                : 2)Non-primitive

   Primitive: byte - 1 byte(-128 to 127)
            : short - 2 byte
            : int - 4 byte
            : long - 8 byte
            : float - 4 byte
            : double - 8 byte
            : char - 2 byte
            : boolean - 1 bit


    Non-Primitive: String, Array...
*/
public class Datatype {
    static void main() {
        byte num1 = 100;
        short num2 = 15000;
        int num3 = 50000;
        long num4 = 15000000000L;
        float num5 = 5.75f;
        double num6 = 19.99d;
        boolean bool = true;
        char ch = 'A';
    }
}
